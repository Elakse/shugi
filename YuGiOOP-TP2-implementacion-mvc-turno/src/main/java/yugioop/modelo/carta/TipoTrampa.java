package yugioop.modelo.carta;

/**
 * Enum que define los diferentes tipos de cartas trampa en Yu-Gi-Oh.
 */
public enum TipoTrampa {
    NORMAL,      // Trampas normales (se usan una vez y van al cementerio)
    CONTINUA,    // Trampas continuas (permanecen en el campo)
    CONTRAEFECTO // Trampas de contraefecto (utilizadas en respuesta a otras acciones)
}
